   public void loadTable() {

        try {
            List<Libri> lista = lr.findAll();
            ltm.addList(lista);
            TableLibri.setModel(ltm);
            ltm.fireTableDataChanged();

        } catch (eLibraryException elx) {
            Logger.getLogger(LibriForm.class.getName()).log(Level.SEVERE, null, elx);
        }
    }
   public void tabelaSelectedIndexChange() {

        ListSelectionModel rowSM = TableLibri.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (e.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Libri libri = ltm.getLibri(selectedIndex);
                    txtId.setText(libri.getLibriId().toString());
                    txtEmri.setText(libri.getTitulli());
                    NrFaqeve.setText(libri.getNumriIFaqeve().toString());
                   
                    

                }
            }
        });
    }
   
    public void loadComboBox() {
        try {
            List <Autori> lista = ar.findAll();
            List <Lloji> list = llr.findAll();
            
            acbm.add(lista);
            lcbm.add(list);
            
            ComboBoxAutori.setModel(acbm);
            ComboBoxAutori.repaint();
            ComboBoxLloji.setModel(lcbm);
            ComboBoxLloji.repaint();
                       
        } catch (eLibraryException elx) {
            JOptionPane.showMessageDialog(this, elx.getMessage());
        }
    }
   
