    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed

        int row = TableLibri.getSelectedRow();
        Libri l = this.ltm.getLibri(row);

        l.setLibriId(Integer.parseInt(txtId.getText()));
        l.setTitulli(txtEmri.getText());
        l.setNumriIFaqeve(Integer.parseInt(NrFaqeve.getText()));
        l.setFkIdAutori((Autori) acbm.getSelectedItem());
        l.setFkLlojiId((Lloji) lcbm.getSelectedItem());
        l.setBotusi(txtBotuesi.getText());

        clear();
        loadTable();
        try{
            lr.edit(l);
        }catch(eLibraryException elx){
            Logger.getLogger(LibriForm.class.getName()).log(Level.SEVERE, null,elx);
        }

        JOptionPane.showMessageDialog(this, "Libri u editua me sukses");
    }//GEN-LAST:event_btnupdateActionPerformed

