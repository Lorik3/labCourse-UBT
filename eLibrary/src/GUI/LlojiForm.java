   public void tabelaSelectedIndexChange() {

        ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (e.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Lloji lloji = ltm.getLloji(selectedIndex);
                    txtID.setText(lloji.getLlojiId().toString());
                    txtEmri.setText(lloji.getEmri());
                    

                }
            }
        });
    }

        public void loadTable() {

        try {
            List<Lloji> lista = lr.findAll();
            ltm.addList(lista);
            table.setModel(ltm);
            ltm.fireTableDataChanged();

        } catch (eLibraryException elx) {
            Logger.getLogger(LlojiForm.class.getName()).log(Level.SEVERE, null, elx);
        }
    }
    
    public void clear() {
        table.clearSelection();
        txtID.setText("");
        txtEmri.setText("");
        

    }

    

